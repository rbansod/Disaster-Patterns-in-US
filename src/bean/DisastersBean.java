package bean;


public class DisastersBean{
	
	String stateCode;
	String disastersTypeCount;
	
	public String getStateCode() {
		return stateCode;
	}
	public void setStateCode(String stateCode) {
		this.stateCode = stateCode;
	}
	public String getDisastersTypeCount() {
		return disastersTypeCount;
	}
	public void setDisastersTypeCount(String disastersTypeCount) {
		this.disastersTypeCount = disastersTypeCount;
	}
	
	

}
